for filename in target_files:
    filepath = os.path.join(directory, filename)
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    dash_active = ' active' if filename == 'dashboard.html' else ''
    events_active = ' active' if filename == 'my-events.html' else ''
    settings_active = ' active' if filename == 'settings.html' else ''
    
    new_nav = sidebar_replacement.format(dash_active=dash_active, events_active=events_active, settings_active=settings_active)
    
    # regex replace the entire <nav class="sidebar-nav"> ... </nav> block
    pattern = re.compile(r'<nav class="sidebar-nav">.*?</nav>', re.DOTALL)
    new_content = pattern.sub(new_nav, content)
    
    if new_content != content:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        print(f"Updated sidebar in {filename}")
